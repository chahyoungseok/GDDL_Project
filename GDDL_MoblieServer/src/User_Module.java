

public class User_Module {
	private String userName;
	private String userId;
	private String userPass;
	private String userSchoolNumber;
	private String userGender;
	private String userMajor;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserPass() {
		return userPass;
	}
	public void setUserPass(String userPass) {
		this.userPass = userPass;
	}
	public String getUserSchoolNumber() {
		return userSchoolNumber;
	}
	public void setUserSchoolNumber(String userSchoolNumber) {
		this.userSchoolNumber = userSchoolNumber;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public String getUserMajor() {
		return userMajor;
	}
	public void setUserMajor(String userMajor) {
		this.userMajor = userMajor;
	}
	

	
}
